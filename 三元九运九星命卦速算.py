#三元九运速算法

#上元甲子一宫连，中元起巽下兑间；上五中二下八女，男逆女顺起根源。

#男命
nan={4:'乾金',5:'坤土',6:'巽木',7:'震木',8:'坤土',0:'坎水',1:'离火',2:'艮土',3:'兑金'}
#女命
nv={4:'乾金',3:'坤土',2:'巽木',1:'震木',0:'艮土',8:'坎水',7:'离火',6:'艮土',5:'兑金'}

#1900年男命是坎水年，女命是艮土年，用本年或者出生年减去1864年，除以9，余数对应的上表就是年命。

print('请输入年份')
year=int(input())

a=year-1900

b=a%9

c=nan[b]
d=nv[b]

print(str(year)+'年男命为'+c+'，女命为'+d)
